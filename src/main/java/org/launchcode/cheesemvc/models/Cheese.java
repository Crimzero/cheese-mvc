package org.launchcode.cheesemvc.models;

public class Cheese {

    public String name;
    public String description;

    public void setName() {
        String name = this.name;
    }

    public String getName() {
        return name;
    }

    public void setDesc() {
        String description = this.description;
    }

    public String getDesc() {
        return description;
    }
}
